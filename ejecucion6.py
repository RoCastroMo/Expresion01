#Cantidad de petalos
def how_much_i_love_you(nb_petals):
    if nb_petals == 3:
        return "a lot"
    elif nb_petals == 7:
        return "I love you"
    elif nb_petals == 6:
        return "not at all"


print(how_much_i_love_you(7))
print(how_much_i_love_you(3))
print(how_much_i_love_you(6))
